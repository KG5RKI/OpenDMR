#ifndef ADC1_H
#define ADC1_H

#include <stdint.h>
#include "stm32f4xx.h"

void adc1_init();

void adc1_shutdown();

/**
 * Get current measurement of a given channel, mapped as below:
 * - CH0 volume level
 * - CH1 battery voltage
 * - CH2 vox level
 * - CH3 RSSI level
 * @param ch: channel number, between 0 and 3.
 * @return current value of the specified channel.
 */
uint16_t adc1_getMeasurement(uint8_t ch);

#endif /* ADC1_H */
